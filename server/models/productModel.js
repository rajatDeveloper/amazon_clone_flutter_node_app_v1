const mongoose = require("mongoose");
const ratingSchema = require("./ratingModel");
const {Schema} = require("mongoose");

const productSchema = new Schema({
  name: {
    type: String,
    required: true,
    trim: true,
  },
  description: {
    type: String,
    required: true,
    trim: true,
  },
  images: [
    {
      type: String,
      required: true,
    },
  ],
  quantity: {
    type: Number,
    required: true,
  },
  price: {
    type: Number,
    required: true,
  },
  category: {
    type: String,
    required: true,
  },
  // ratings: [ratingSchema],
});


module.exports = mongoose.model("Product",productSchema)

